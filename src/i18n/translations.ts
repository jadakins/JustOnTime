export type TranslationKey = keyof typeof translations.en;

// Hook-like function for getting translations
export function getTranslation(language: 'en' | 'id', key: TranslationKey): string {
  return translations[language][key] || key;
}

// Helper to get all translations for a language
export function useTranslations(language: 'en' | 'id') {
  return {
    t: (key: TranslationKey) => getTranslation(language, key),
    language,
  };
}
